package com.zaotao.daylucky.module.entity;

public class LuckyWeekEntity {
    private String x;
    private int y;

    public LuckyWeekEntity(String x, int y) {
        this.x = x;
        this.y = y;
    }

    public String getX() {
        return x;
    }


    public int getY() {
        return y;
    }

}
